#include"ft_printf.h"

void	ft_px(unsigned long int nb, int *j)
{
	char	*base;

	base = "0123456789abcdef";
	if (nb < 16)
	{
		write(1, &base[nb], 1);
		*j += 1;
	}
	else
	{
		ft_px((nb / 16), j);
		ft_px((nb % 16), j);
	}
}
